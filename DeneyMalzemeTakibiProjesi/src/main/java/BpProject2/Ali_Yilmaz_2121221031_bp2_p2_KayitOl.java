    private void btn_KayitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_KayitActionPerformed
       try {
        Ali_Yilmaz_2121221031_bp2_p2_Users users = new Ali_Yilmaz_2121221031_bp2_p2_Users();
        users.name_surname = txt_name_surname.getText();
        users.User_Id = txt_UserId.getText();
        users.password = txt_password.getText();

        if (radio_Admin.isSelected()) {
            users.Position = radio_Admin.getText();
        } else if (radio_User.isSelected()) {
            users.Position = radio_User.getText();
        } else {
            throw new Exception("Admin veya User olma durumu seçilmeli !");
        }

        validateNameSurname(txt_name_surname.getText());
        validateUserId(txt_UserId.getText());
        validatePassword(txt_password.getText());

        if (Ali_Yilmaz_2121221031_bp2_p2_MalzemeDB.AddUser(users)) {
            JOptionPane.showMessageDialog(this, "Kullanıcı Veri Tabanına Eklendi");
            TabloSifirlama();
        } else {
            JOptionPane.showMessageDialog(this, "Veri Tabanına Ekleme Başarısız");
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }

    }//GEN-LAST:event_btn_KayitActionPerformed

