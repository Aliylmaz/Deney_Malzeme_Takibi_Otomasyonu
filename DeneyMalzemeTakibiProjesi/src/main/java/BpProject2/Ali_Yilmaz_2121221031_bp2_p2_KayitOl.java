    public void TabloSifirlama() {
        txt_name_surname.setText("");
        txt_UserId.setText("");
        txt_password.setText("");
        buttonGroup1.clearSelection();

    }

    public void validateNameSurname(String nameSurname) {
        String regex = "^[A-Za-z\\s]+$";
        if (!nameSurname.matches(regex)) {
            throw new IllegalArgumentException("Geçersiz isim/soyisim formatı");
        }
    }

    public void validateUserId(String userId) {
        String regex = "^[a-zA-Z0-9]+$";
        if (!userId.matches(regex)) {
            throw new IllegalArgumentException("Geçersiz kullanıcı ID formatı");
        }
    }

    public void validatePassword(String password) {
    String regex = "^[A-Za-z\\d]{5,20}$";


if (!password.matches(regex)) {
    throw new IllegalArgumentException("Geçersiz şifre formatı");
}

    }

