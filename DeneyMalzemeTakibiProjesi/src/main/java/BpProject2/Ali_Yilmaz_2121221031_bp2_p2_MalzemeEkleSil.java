    private void btn_SilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SilActionPerformed

        String kullaniciAdi = Ali_Yilmaz_2121221031_bp2_p2_GirisEkrani.UserId;
        String sifre = Ali_Yilmaz_2121221031_bp2_p2_GirisEkrani.Password;
        String position = Ali_Yilmaz_2121221031_bp2_p2_MalzemeDB.getUserPosition(kullaniciAdi, sifre);

        if (position != null) {
            // Kullanıcı doğrulandı, işleme devam et
            if (position.equals("Admin")) {
                int choice = JOptionPane.showConfirmDialog(this, "Gerçekten silmek istiyor musunuz? Bu işlem geri alınamaz.", "Malzeme Silme", JOptionPane.YES_NO_OPTION);
                if (choice == JOptionPane.YES_OPTION) {
                    int selectedRow = table_Sil.getSelectedRow();

                    // Satırın verilerini al
                    int id = Integer.parseInt(table_Sil.getValueAt(selectedRow, 0).toString());
                    // Diğer verileri de ihtiyaca göre al

                    // Veriyi sil
                    boolean isDeleted = Ali_Yilmaz_2121221031_bp2_p2_MalzemeDB.DeleteById(id);
                    if (isDeleted) {
                        // Veri silme işlemi başarılıysa
                        JOptionPane.showMessageDialog(this, "Veri başarıyla silindi.");

                        // Tabloyu güncelle
                        updateTable();
                    } else {
                        // Veri silme işlemi başarısızsa
                        JOptionPane.showMessageDialog(this, "Veri silme işlemi başarısız.");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Silmek için Admin olmanız gerekli", "Silme işlemi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Kullanıcı adı veya şifre hatalı", "Hatalı Giriş", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btn_SilActionPerformed

