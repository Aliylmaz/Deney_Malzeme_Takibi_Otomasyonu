    // ID için geçerlilik kontrolü
    private boolean isValidId(String idText) {
        String regex = "^[1-9]\\d*$"; // 0'dan farklı pozitif tam sayılar
        return idText.matches(regex);
    }

// Malzeme adı için geçerlilik kontrolü
    private boolean isValidMalzemeAdi(String malzemeAdi) {
        String regex = "^[A-Za-z\\s]+$"; // Sadece harf ve boşluk içeren metin
        return malzemeAdi.matches(regex) && !malzemeAdi.trim().isEmpty();
    }

